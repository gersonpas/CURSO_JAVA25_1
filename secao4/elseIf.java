public class elseIf {
    public static void main(String[] args) {
        double nota = 10;

        if (nota == 10) {
            System.out.println("Você tirou a nota Máxima!"); 
        
        } if (nota == 8) {
            System.out.println("Nota muito boa!");
            
        } if (nota == 7) {
            System.out.println("Abaixo da Média");
            
        }else {
            System.out.println("Recuperação !");

        }           

        }
            
        }
    
    
    

