package secao4;

public class operadorLogicos {
    public static void main(String[] args) {
        
        int diaDaSemana = 1;

 
        switch (diaDaSemana) {
            case 1:
            System.out.println("Domingo");              
                break;
            case 2:
            System.out.println("Segunda-Feira");
                break;
            case 3:
            System.out.println("Terça-Feira");
                break;
            default:
            System.out.println("Número não encontrado!");
            
        }


    }
}
