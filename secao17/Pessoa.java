package secao17;

public class Pessoa {
    private String nome;
    private int idade;

    public Pessoa(String string, int i) {
        this.nome = string;
        this.idade = i;

    }
        
    public void setNome(String nome){
        this.nome = nome;
    }
    public String getNome(){
        return nome;
    }
    public void setIdade(int idade){
        this.idade = idade;
    }
    public int getIdade(){
        return idade;
        
    }        

}
